public class User {
    String userName;
    String password;

    public User() {

    }

    public void setUserName(String userNmae) {
        this.userName = userNmae;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getUserName() {
        return userName;
    }

    public String getPassword() {
        return password;
    }
}
